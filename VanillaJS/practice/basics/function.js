// function with no parameters
function funcName() {
  return;
}
// or
const funcName = () => console.log("anything");

// regular function

function functionName(parameters) {
  return; // function body
}

// arrow function
const functionName = (parameters) => {
  return; // function body
};
// or
const functionName = (parameters) => console.log("doSomething"); // function body

// function with more than one parameters
function fName(p1, p2, p3) {
  return; // function body
}
// or
const fName = (p1, p2, p3) => console.log("doSomething");
